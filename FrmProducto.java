    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
     boolean linea = false;
     boolean encontrado = false;
     if(rbElectrodomestico.isSelected() && !rbLineaBlanca.isSelected()){
         linea = true;
         cantidadE++;
         codigoP =("Ele"+txtCodigo);
         Electrodomestico nuevoElectrodomestico = new Electrodomestico("Ele"+txtCodigo.getText(),cantidadE, txtNombre.getText(),Double.parseDouble(txtPrecio.getText()),linea);
         
         for(int i=0; i<electrodomesticos.size();i++){
             if(electrodomesticos.get(i).getCodProducto().equals(nuevoElectrodomestico.getCodProducto())){
                 electrodomesticos.set(1,nuevoElectrodomestico);
                 encontrado = true;
                 break;
             }
         }
         electrodomesticos.add(nuevoElectrodomestico);       
     }else if(!rbElectrodomestico.isSelected() && rbLineaBlanca.isSelected()){
         linea = false;
                  cantidadLb++;
              codigoP =("LBl"+txtCodigo);
         LineaBlanca nuevoLineaBl = new LineaBlanca("LBl"+txtCodigo.getText(),cantidadLb,txtNombre.getText(),Double.parseDouble(txtPrecio.getText()),linea);
          for(int i=0; i<productosLb.size();i++){
             if(productosLb.get(i).getCodProducto().equals(nuevoLineaBl.getCodProducto())){
                 productosLb.set(1,nuevoLineaBl);
                 encontrado = true;
                 break;
             }
           }
        productosLb.add(nuevoLineaBl);
     }
    
     if(encontrado){
         JOptionPane.showMessageDialog(this," Producto se a modificado","Modificar Producto", JOptionPane.INFORMATION_MESSAGE);
     }else{
         JOptionPane.showMessageDialog(this," Producto insertado", "Nuevo Producto",JOptionPane.INFORMATION_MESSAGE);

     }
     limpiarCampos();
    System.out.println(Arrays.asList(electrodomesticos));
    System.out.println(Arrays.asList(productosLb));
    }//GEN-LAST:event_btnGuardarActionPerformed

