    private void bBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bBuscarActionPerformed
        boolean encontrado = false;
    codigoP = txtCodigo.getText();  
    
    for(int i=0;i<3;i++){
    if(codigoP.charAt(i)!=clave1.charAt(i)){
          if(codigoP.charAt(i)!=clave2.charAt(i)){
            JOptionPane.showMessageDialog(this, "Código Invalido, verificalo.","Busqueda Producto",JOptionPane.WARNING_MESSAGE);
            limpiarCampos();
        }else{
          if(codigoP.charAt(i)==clave1.charAt(i)){
              for(LineaBlanca productoLb: productosLb){
              if(productoLb.getCodProducto().equals(txtCodigo.getText())){
                  txtCodigo.setText(productoLb.getCodProducto());
                  txtInv.setText(Integer.toString(productoLb.getCantidad()));
                  txtNombre.setText(productoLb.getNombrePro());
                  txtPrecio.setText(String.valueOf(productoLb.getPrecio()));
                  rbLineaBlanca.setSelected(true);
                  rbElectrodomestico.setSelected(false);
                }
                encontrado = true;
                break;
              }
            }else{
              for(Electrodomestico electrodomestico: electrodomesticos){
              if(electrodomestico.getCodProducto().equals(txtCodigo.getText())){
                  txtCodigo.setText(electrodomestico.getCodProducto());
                  txtInv.setText(Integer.toString(electrodomestico.getCantidad()));
                  txtNombre.setText(electrodomestico.getNombrePro());
                  txtPrecio.setText(String.valueOf(electrodomestico.getPrecio()));
                  rbLineaBlanca.setSelected(false);
                  rbElectrodomestico.setSelected(true);
                }
                encontrado = true;
                break;
              }    
            }
          } 
        }  
    }
    }//GEN-LAST:event_bBuscarActionPerformed

    private void bEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEliminarActionPerformed
    boolean encontrado = false;
    for(int i=0;i<3;i++){
        if(codigoP.charAt(i)!=clave1.charAt(i)){
             if(codigoP.charAt(i)!=clave2.charAt(i)){
              JOptionPane.showMessageDialog(this, "Código Invalido, verificalo.","Busqueda Producto",JOptionPane.WARNING_MESSAGE);
              limpiarCampos();
            }else{
                 if(codigoP.charAt(i)==clave1.charAt(i)){
                    for(LineaBlanca productoLb: productosLb){
                        if(productoLb.getCodProducto().equals(txtCodigo.getText())){
                            productosLb.remove(productoLb);
                            encontrado = true;
                            JOptionPane.showMessageDialog(this," Producto eliminado correctamente","Eliminar Producto",JOptionPane.INFORMATION_MESSAGE);
                            limpiarCampos();
                            break;
                            }
                    }
            
                 }else{
                        for(Electrodomestico electrodomestico: electrodomesticos){
                        if(electrodomestico.getCodProducto().equals(txtCodigo.getText())){
                            electrodomesticos.remove(electrodomestico);
                            encontrado = true;
                            JOptionPane.showMessageDialog(this," Producto eliminado correctamente","Eliminar Producto",JOptionPane.INFORMATION_MESSAGE);
                            limpiarCampos();
                            break;
                            }
                        }
                    }
                }
        }
    }
    }//GEN-LAST:event_bEliminarActionPerformed

